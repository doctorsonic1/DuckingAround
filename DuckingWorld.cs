        public static bool CheckFlat(
  int startX,
  int startY,
  int width,
  float threshold,
  int goingDownWeight = 0,
  int goingUpWeight = 0)
        {
            if (!WorldGen.SolidTile(startX + width, startY))
                return false;
            float num1 = 0.0f;
            for (int index = 0; index < width; ++index)
            {
                if (startX + index >= Main.maxTilesX)
                    return false;
                for (int j = startY - 1; j > startY - 100; --j)
                {
                    if (WorldGen.SolidTile(startX + index, j))
                        return false;
                }
                int num2 = 0;
                bool flag = WorldGen.SolidTile(startX + index, startY);
                int num3 = num2 + (flag ? goingUpWeight : goingDownWeight);
                while (flag && WorldGen.SolidTile(startX + index, startY - num3) || !flag && !WorldGen.SolidTile(startX + index, startY + num3))
                    ++num3;
                if (flag)
                    --num3;
                num1 += (float)num3;
            }
            return (double)num1 / (double)width <= (double)threshold;
        }
        private void MakeWells()
        {
            float widthScale = Main.maxTilesX / 4200f;
            int numberToGenerate = 1;
            for (int k = 0; k < numberToGenerate; k++)
            {
                bool success = false;
                int attempts = 0;
                while (!success)
                {
                    attempts++;
                    if (attempts > 1000)
                    {
                        success = true;
                        continue;
                    }
                    int i = WorldGen.genRand.Next(300, Main.maxTilesX - 300);
                    if (i <= Main.maxTilesX / 2 - 50 || i >= Main.maxTilesX / 2 + 50)
                    {
                        int j = 0;
                        while (!Main.tile[i, j].active() && (double)j < Main.worldSurface)
                        {
                            j++;
                        }
                        if (Main.tile[i, j].type == TileID.Dirt)
                        {
                            j--;
                            if (j > 150)
                            {
                                bool placementOK = true;
                                for (int l = i - 4; l < i + 4; l++)
                                {
                                    for (int m = j - 6; m < j + 20; m++)
                                    {
                                        if (Main.tile[l, m].active())
                                        {
                                            int type = (int)Main.tile[l, m].type;
                                            if (type == TileID.BlueDungeonBrick || type == TileID.GreenDungeonBrick || type == TileID.PinkDungeonBrick || type == TileID.Cloud || type == TileID.RainCloud)
                                            {
                                                placementOK = false;
                                            }
                                        }
                                    }
                                }
                                if (placementOK)
                                {
                                    success = PlaceWell(i, j);
                                }
                            }
                        }
                    }
                }
            }
        }

