        public bool PlaceWell(int i, int j)
        {
            if (!WorldGen.SolidTile(i, j + 1))
            {
                return false;
            }
            if (Main.tile[i, j].active())
            {
                return false;
            }
            if (j < 150)
            {
                return false;
            }

            for (int y = 0; y < _labTiles.GetLength(0); y++)
            {
                for (int x = 0; x < _labTiles.GetLength(1); x++)
                {
                    int k = i - 3 + x;
                    int l = j - 6 + y;
                    if (WorldGen.InWorld(k, l, 30))
                    {
                        Tile tile = Framing.GetTileSafely(k, l);
                        switch (_labTiles[y, x])
                        {
                            case 1:
                                tile.type = TileID.Pearlstone;
                                tile.color(PaintID.Blue);
                                tile.active(true);                                
                                break;
                            case 2:
                                tile.type = TileID.RedBrick;
                                tile.active(true);
                                tile.color(PaintID.Blue);
                                tile.halfBrick(true);
                                break;
                            case 3:
                                tile.type = TileID.RedBrick;
                                tile.active(true);
                                tile.color(PaintID.Blue);
                                tile.slope(2);
                                break;
                            case 4:
                                tile.type = TileID.RedBrick;
                                tile.active(true);
                                tile.color(PaintID.Blue);
                                tile.slope(1);
                                break;
                            case 5:
                                tile.active(false);
                                tile.color(PaintID.Blue);
                                break;
                            case 6:
                                tile.type = TileID.Rope;
                                tile.color(PaintID.Blue);
                                tile.active(true);
                                break;
                        }
                        switch (_labWalls[y, x])
                        {
                            case 1:
                                tile.wall = WallID.RedBrick;
                                break;
                        }
                    }
                }
            }
            return true;
        }
